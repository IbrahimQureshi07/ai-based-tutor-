/**
 * Reads each "Content Assembling Salesperson(...).csv" in Backend/ and emits SQL
 * that sets questions.subject from the filename (source of truth).
 *
 * Run: node Backend/scripts/generate-subject-fix-sql.mjs > Backend/sql/fix_subjects_from_csv.sql
 * Then run the SQL in Supabase SQL Editor (backup first).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { parse } from 'csv-parse/sync';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const backendDir = path.join(__dirname, '..');

function sqlEscape(str) {
  return String(str).replace(/'/g, "''");
}

function subjectFromFilename(name) {
  const m = name.match(/Content Assembling Salesperson\(([^)]+)\)\.csv$/);
  if (!m) return null;
  return m[1].trim();
}

const files = fs
  .readdirSync(backendDir)
  .filter((f) => f.startsWith('Content Assembling Salesperson') && f.endsWith('.csv'))
  .sort();

console.log('-- Generated by generate-subject-fix-sql.mjs');
console.log('-- Sets subject from each CSV filename so A5 math rows are not tagged as A1.');
console.log('BEGIN;');
console.log('');

let count = 0;
for (const file of files) {
  const subjectLabel = subjectFromFilename(file);
  if (!subjectLabel) {
    console.error(`// skip (no subject in name): ${file}`);
    continue;
  }

  const fullPath = path.join(backendDir, file);
  let raw = fs.readFileSync(fullPath, 'utf8');
  if (raw.charCodeAt(0) === 0xfeff) raw = raw.slice(1);

  let records;
  try {
    records = parse(raw, {
      columns: true,
      skip_empty_lines: true,
      relax_quotes: true,
      relax_column_count: true,
      trim: true,
    });
  } catch (e) {
    console.error(`// ERROR parsing ${file}: ${e.message}`);
    continue;
  }

  const seen = new Set();
  for (const row of records) {
    const q = row.Question;
    if (!q || typeof q !== 'string') continue;
    const questionText = q.trim();
    if (!questionText) continue;
    if (seen.has(questionText)) continue;
    seen.add(questionText);

    const sub = sqlEscape(subjectLabel);
    const qu = sqlEscape(questionText);
    console.log(
      `UPDATE public.questions SET subject = '${sub}' WHERE "Question" = '${qu}';`
    );
    count++;
  }
}

console.log('');
console.log('COMMIT;');
console.error(`// ${count} UPDATE statements emitted.`);
