/**
 * Generates LIKE-prefix-based UPDATE SQL for the 5 subjects that failed
 * exact-match SQL (A3, A6, B3, B5, B6).
 *
 * Run:
 *   node Backend/scripts/generate-failing-subjects-sql.mjs
 *   (output is written to Backend/sql/fix_failing_subjects.sql)
 *
 * Uses WHERE "Question" ILIKE 'first 40 chars%' — resilient to encoding
 * differences (non-breaking spaces, curly quotes, etc.) since we only
 * match the START of each question text.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { parse } from 'csv-parse/sync';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const backendDir = path.join(__dirname, '..');

const FAILING = [
  { file: 'Content Assembling Salesperson(A3 Financing).csv',             subject: 'A3 Financing' },
  { file: 'Content Assembling Salesperson(A6 Closing and Settlem).csv',   subject: 'A6 Closing and Settlem' },
  { file: 'Content Assembling Salesperson(B3 Agency).csv',                subject: 'B3 Agency' },
  { file: 'Content Assembling Salesperson(B5 South Carolina Real Estate ).csv', subject: 'B5 South Carolina Real Estate' },
  { file: 'Content Assembling Salesperson(B6 Governmental Controls).csv', subject: 'B6 Governmental Controls' },
];

/** Length of prefix to use for LIKE match. */
const PREFIX_LEN = 45;

function sqlStr(str) {
  return String(str).replace(/'/g, "''");
}

/** Normalise to plain ASCII-ish: collapse whitespace, remove non-printable */
function cleanPrefix(str) {
  return str
    .replace(/[\u00A0\u202F\u2009\u200B]/g, ' ')  // non-breaking / thin spaces
    .replace(/[\u2018\u2019]/g, "'")               // curly single quotes
    .replace(/[\u201C\u201D]/g, '"')               // curly double quotes
    .replace(/\s+/g, ' ')
    .trim();
}

const lines = [];
lines.push('-- Generated by generate-failing-subjects-sql.mjs');
lines.push('-- Uses ILIKE prefix matching to survive encoding differences in DB vs CSV.');
lines.push('-- Safe to run multiple times (idempotent SET).');
lines.push('BEGIN;');
lines.push('');

let totalUpdates = 0;

for (const { file, subject } of FAILING) {
  const fullPath = path.join(backendDir, file);
  let raw;
  try {
    raw = fs.readFileSync(fullPath, 'utf8');
  } catch {
    lines.push(`-- SKIPPED (file not found): ${file}`);
    continue;
  }
  if (raw.charCodeAt(0) === 0xfeff) raw = raw.slice(1); // strip BOM

  let records;
  try {
    records = parse(raw, {
      columns: true,
      skip_empty_lines: true,
      relax_quotes: true,
      relax_column_count: true,
      trim: true,
    });
  } catch (e) {
    lines.push(`-- ERROR parsing ${file}: ${e.message}`);
    continue;
  }

  lines.push(`-- ===== ${subject} =====`);
  const seen = new Set();
  let count = 0;

  for (const row of records) {
    const q = row.Question;
    if (!q || typeof q !== 'string') continue;
    const questionText = q.trim();
    if (!questionText) continue;

    const cleaned = cleanPrefix(questionText);
    if (!cleaned) continue;

    // Use a prefix of PREFIX_LEN chars; if question is shorter use the whole thing.
    const prefix = cleaned.slice(0, PREFIX_LEN);
    if (seen.has(prefix)) continue;
    seen.add(prefix);

    const sub = sqlStr(subject);
    const pfx = sqlStr(prefix);
    // Use ILIKE so case differences don't matter either
    lines.push(`UPDATE public.questions SET subject = '${sub}' WHERE "Question" ILIKE '${pfx}%';`);
    count++;
    totalUpdates++;
  }

  lines.push(`-- ${count} statements for ${subject}`);
  lines.push('');
}

lines.push('COMMIT;');

const outPath = path.join(backendDir, 'sql', 'fix_failing_subjects.sql');
fs.mkdirSync(path.join(backendDir, 'sql'), { recursive: true });
fs.writeFileSync(outPath, lines.join('\n'), 'utf8');

console.log(`Done. ${totalUpdates} UPDATE statements → ${outPath}`);
